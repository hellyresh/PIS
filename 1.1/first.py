print(
    'Каждый день я пробегаю «следующую степень двойки» км. Сколько дней пройдет, пока я в сумме пробегу 1000 км? '
    '10000 км?')


def z1(n):
    day = 0
    km = 0
    while km < n:
        km += 2 ** day
        day += 1
    return day


print("Пока я пробегу 1000 км пройдет ", z1(1000), " дней")
print("Пока я пробегу 10000 км пройдет ", z1(10000), " дней")
