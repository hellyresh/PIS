print("Задание 4")
print("Начав тренировки, спортсмен в первый день пробежал 10 км. Каждый следующий день он увеличивал дневную норму \n"
      "на 10% от нормы предыдущего дня. Через сколько дней: \nа) спортсмен будет пробегать в день больше 20 км; "
      "\nb) пробежит суммарный путь 100 км.")


def z4():
    res = [0, 0]
    km = 10
    day = 1
    sum_km = 0
    while True:
        sum_km += km
        if km > 20 and res[0] == 0:
            res[0] = day
        if sum_km >= 100 and res[1] == 0:
            res[1] = day
        if res[0] > 0 and res[1] > 0:
            break
        km *= 1.1
        day += 1
    return res


res = z4()
print("Ответ:\na)", res[0], "дней\nb)", res[1], "дней")
