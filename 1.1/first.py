print("Начав тренировки, спортсмен в первый день пробежал 10 км. Для увеличения выносливости ему необходимо повышать \n"
      "норму бега через одну тренировку на 15% от нормы предыдущей тренировки. Спортсмен тренируется каждый день. \n"
      "Какой суммарный путь он пробежит за 30 дней.")


def z3():
    day = 1
    km = 10
    sum_km = 0
    while day <= 30:
        if day % 2 == 0:  # на каждой второй тренировке повышаем, начиная со второго дня
            km *= 1.15
        sum_km += km
        day += 1
    return sum_km


print("За 30 дней спортсмен пробежит", z3(), "км")
